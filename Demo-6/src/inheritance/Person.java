package inheritance;

public class Person {
    private String name;
    private String hairColour;
    private boolean isHappy;
    private int age;

    public Person(String name, String hairColour, boolean isHappy, int age) {
        this.name = name;
        this.hairColour = hairColour;
        this.isHappy = isHappy;
        this.age = age;
    }
}

